import { createHash } from 'node:crypto';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, isAbsolute, relative, resolve } from 'node:path';
import { glob } from 'tinyglobby';
import ts from 'typescript';
import { resolveAssetRequest } from './utils';
import type { NormalizedAssetGenConfig } from './config';

export type AssetDefinition = {
  filePath: string;
  importSpecifier: string;
};

export type GeneratedAssetGenOutput = {
  assets: AssetDefinition[];
  outFile: string;
};

const toPosixPath = (value: string): string => value.replaceAll('\\', '/');
const toImportSpecifier = (fromFile: string, target: string): string => {
  if (!isAbsolute(target) && !target.startsWith('.')) {
    return target;
  }

  const relativePath = toPosixPath(relative(dirname(fromFile), target));
  const importPath = relativePath.replace(/\.(?:c|m)?[jt]sx?$/, '');
  return importPath.startsWith('.') ? importPath : `./${importPath}`;
};

const resolveIncludeFiles = async (root: string, include: readonly string[]): Promise<string[]> =>
  glob([...include], {
    absolute: true,
    cwd: root,
    expandDirectories: false,
    onlyFiles: true,
  });

const getSourceKind = (filePath: string): ts.ScriptKind => {
  if (filePath.endsWith('.tsx')) {
    return ts.ScriptKind.TSX;
  }

  if (filePath.endsWith('.jsx')) {
    return ts.ScriptKind.JSX;
  }

  return ts.ScriptKind.TS;
};

const resolveAssetImport = (
  outFile: string,
  sourceFile: string,
  specifier: string,
): string | undefined => {
  const request = resolveAssetRequest(specifier);
  if (!request) {
    return undefined;
  }

  const queryIndex = request.indexOf('?');
  const source = request.slice(0, queryIndex);
  const query = request.slice(queryIndex);
  if (!source.startsWith('.')) {
    return request;
  }

  const target = resolve(dirname(sourceFile), source);
  const importPath = toPosixPath(relative(dirname(outFile), target));
  const relativeSpecifier = importPath.startsWith('.') ? importPath : `./${importPath}`;
  return `${relativeSpecifier}${query}`;
};

const collectAssetsFromFile = async (
  filePath: string,
  outFile: string,
): Promise<AssetDefinition[]> => {
  const sourceText = await readFile(filePath, 'utf8');
  const sourceFile = ts.createSourceFile(
    filePath,
    sourceText,
    ts.ScriptTarget.Latest,
    true,
    getSourceKind(filePath),
  );

  const assets: AssetDefinition[] = [];

  for (const statement of sourceFile.statements) {
    if (!ts.isImportDeclaration(statement) || !ts.isStringLiteral(statement.moduleSpecifier)) {
      continue;
    }

    const importSpecifier = resolveAssetImport(outFile, filePath, statement.moduleSpecifier.text);
    if (importSpecifier) {
      assets.push({ filePath, importSpecifier });
    }
  }

  return assets;
};

export const collectAssets = async (
  root: string,
  config: NormalizedAssetGenConfig,
): Promise<AssetDefinition[]> => {
  const files = (await resolveIncludeFiles(root, config.include))
    .filter(file => file !== config.outFile)
    .sort();

  const discoveredAssets = (
    await Promise.all(files.map(file => collectAssetsFromFile(file, config.outFile)))
  ).flat();

  const uniqueAssets = new Map<string, AssetDefinition>();
  for (const asset of discoveredAssets) {
    uniqueAssets.set(asset.importSpecifier, asset);
  }

  return [...uniqueAssets.values()].sort((left, right) =>
    left.importSpecifier.localeCompare(right.importSpecifier),
  );
};

const getAssetIdentifier = (specifier: string): string =>
  `asset_${createHash('sha256').update(specifier).digest('hex').slice(0, 8)}`;

export const renderGeneratedAssets = (
  config: NormalizedAssetGenConfig,
  assets: AssetDefinition[],
): string => {
  const identifiers = new Map<string, string>();
  const lines = ['// This file is generated by assetgen. Do not edit manually.'];

  for (const asset of assets) {
    const identifier = getAssetIdentifier(asset.importSpecifier);
    const duplicateSpecifier = identifiers.get(identifier);
    if (duplicateSpecifier && duplicateSpecifier !== asset.importSpecifier) {
      throw new Error(
        `Generated asset identifier collision for ${duplicateSpecifier} and ${asset.importSpecifier}.`,
      );
    }

    identifiers.set(identifier, asset.importSpecifier);
    lines.push(`import ${identifier} from ${JSON.stringify(asset.importSpecifier)};`);
  }

  const assetUtilSpecifier = JSON.stringify(toImportSpecifier(config.outFile, config.loaderModule));
  lines.push(`import { createAssetsLoader } from ${assetUtilSpecifier};`);

  if (config.react) {
    lines.push(
      "import { createContext, useEffect, useState } from 'react';",
      "import type { PropsWithChildren } from 'react';",
    );
  }

  lines.push(
    '',
    'const createRegisteredAssetsLoader = () => {',
    '  const loader = createAssetsLoader();',
  );

  if (assets.length === 0) {
    lines.push('  loader.add([]);');
  } else {
    lines.push(
      '  loader.add([',
      ...assets.map(asset => `    ${getAssetIdentifier(asset.importSpecifier)},`),
      '  ]);',
    );
  }

  lines.push(
    '  return loader;',
    '};',
    '',
    'const globalAssetsLoader = createRegisteredAssetsLoader();',
    'export default globalAssetsLoader;',
  );

  if (config.react) {
    lines.push(
      '',
      'export const AssetsContext = createContext(globalAssetsLoader);',
      '',
      'export const AssetsProvider = ({ children }: PropsWithChildren) => {',
      '  const [assets] = useState(() => createRegisteredAssetsLoader());',
      '  useEffect(() => () => assets.dispose(), [assets]);',
      '',
      '  return <AssetsContext.Provider value={assets}>{children}</AssetsContext.Provider>;',
      '};',
    );
  }

  return `${lines.join('\n')}\n`;
};

export const generateAssets = async ({
  root,
  config,
}: {
  root: string;
  config: NormalizedAssetGenConfig;
}): Promise<GeneratedAssetGenOutput> => {
  const assets = await collectAssets(root, config);
  const contents = renderGeneratedAssets(config, assets);

  await mkdir(dirname(config.outFile), { recursive: true });
  await writeFile(config.outFile, contents);

  return {
    assets,
    outFile: config.outFile,
  };
};
